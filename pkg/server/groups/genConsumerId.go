package groups

import (
	"fmt"

	"golang.org/x/exp/rand"
)

const letterBytes = "abcdefghijklmnopqrstuvwxyzABCDEFGHIJKLMNOPQRSTUVWXYZ1234567890"

func (g Group) RandConsumerId(n int) string {
	b := make([]byte, n)
	for i := range b {
		b[i] = letterBytes[rand.Intn(len(letterBytes))]
	}
	consumerId := fmt.Sprintf("%s-%s", g.GroupId, string(b))
	return consumerId
}
